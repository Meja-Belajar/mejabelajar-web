/**
 * This module provides animation for motion components.
 * @module pageTransitions
 * @exports initial - The initial state of the component
 * @exports animate - The state of the component when it is animating
 * @exports exit - The state of the component when it is exiting
 */
export const initial = {
  opacity: 0,
};

export const animate = {
  opacity: 1,
};

export const exit = {
  opacity: 0,
};
